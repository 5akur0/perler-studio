// Dev tool: bake the share-card QR into src/share-qr.js as a base64 data: URL.
//
// Source: a hand-styled QR SVG (perler-qr.svg at repo root) — rounded dots + the
// 拼豆工坊 center logo. We embed it as a base64 data: URL rather than shipping the
// file because the app runs offline via file://, where drawing an
// <img src="…file.svg"> onto a canvas taints it under the opaque origin, so
// canvas.toBlob()/toDataURL() throws SecurityError. A self-contained data: URL
// image does NOT taint the canvas and works offline. CSP already allows
// `img-src data:`. Re-run after editing the SVG:
//
//   node scripts/gen-share-qr.mjs
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const SHARE_URL = 'https://perler-studio.pages.dev'; // what the QR encodes (for reference)

const DIR = path.dirname(fileURLToPath(import.meta.url));
const SVG = path.join(DIR, '..', 'perler-qr.svg');
const OUT = path.join(DIR, '..', 'src', 'share-qr.js');

const svg = await readFile(SVG, 'utf8');
const b64 = Buffer.from(svg, 'utf8').toString('base64');
const dataUrl = `data:image/svg+xml;base64,${b64}`;

const module = `// GENERATED by scripts/gen-share-qr.mjs — do not hand-edit.
// Share-card QR (SVG) encoding: ${SHARE_URL}
// A base64 data: URL (not a file) so it can be drawn onto an offline file://
// canvas without tainting it. Regenerate via: node scripts/gen-share-qr.mjs
export const SHARE_QR_URL = ${JSON.stringify(SHARE_URL)};
export const SHARE_QR_DATA_URL = ${JSON.stringify(dataUrl)};
`;

await writeFile(OUT, module);
console.log(`wrote ${path.relative(path.join(DIR, '..'), OUT)}  (${dataUrl.length} chars, from perler-qr.svg)`);
